/**
 * 获取打卡统计数据 Mock 函数
 */
export function getCheckinStatisticsMock(params: { projectId?: number; startTime?: string; endTime?: string }) {
  const { projectId, startTime, endTime } = params
  let filteredData = [...checkinRecords]

  // 按项目筛选
  if (projectId) {
    filteredData = filteredData.filter(item => item.projectId === projectId)
  }

  // 按时间范围筛选
  if (startTime) {
    filteredData = filteredData.filter(item => item.checkinTime >= startTime)
  }
  if (endTime) {
    filteredData = filteredData.filter(item => item.checkinTime <= endTime)
  }

  // 统计总打卡次数
  const totalCount = filteredData.length

  // 统计上班打卡次数
  const clockInCount = filteredData.filter(item => item.checkinType === 1).length

  // 统计下班打卡次数
  const clockOutCount = filteredData.filter(item => item.checkinType === 2).length

  // 统计参与人数
  const userIds = new Set(filteredData.map(item => item.userId))
  const userCount = userIds.size

  // 按人员统计
  const userStatsMap = new Map<number, UserCheckinStats>()
  filteredData.forEach(item => {
    if (!userStatsMap.has(item.userId)) {
      userStatsMap.set(item.userId, {
        userId: item.userId,
        userName: item.userName,
        checkinCount: 0,
        workHours: 0,
        records: []
      })
    }
    const stats = userStatsMap.get(item.userId)!
    stats.checkinCount++
    stats.records!.push(item)
  })

  // 计算工作时长（简化计算：每对上下班打卡算8小时）
  userStatsMap.forEach(stats => {
    const userRecords = filteredData.filter(item => item.userId === stats.userId)
    const clockInRecords = userRecords.filter(item => item.checkinType === 1)
    stats.workHours = clockInRecords.length * 8
  })

  const userStats = Array.from(userStatsMap.values())

  // 按项目统计
  const projectStatsMap = new Map<number, ProjectCheckinStats>()
  filteredData.forEach(item => {
    if (!projectStatsMap.has(item.projectId)) {
      projectStatsMap.set(item.projectId, {
        projectId: item.projectId,
        projectName: item.projectName,
        checkinCount: 0,
        userCount: 0
      })
    }
    const stats = projectStatsMap.get(item.projectId)!
    stats.checkinCount++
  })

  // 计算每个项目的参与人数
  projectStatsMap.forEach((stats, projectId) => {
    const projectUserIds = new Set(
      filteredData.filter(item => item.projectId === projectId).map(item => item.userId)
    )
    stats.userCount = projectUserIds.size
  })

  const projectStats = Array.from(projectStatsMap.values())

  // 按日期统计
  const dateStatsMap = new Map<string, DateCheckinStats>()
  filteredData.forEach(item => {
    const date = item.checkinTime.split(' ')[0]
    if (!dateStatsMap.has(date)) {
      dateStatsMap.set(date, {
        date,
        checkinCount: 0,
        userCount: 0
      })
    }
    const stats = dateStatsMap.get(date)!
    stats.checkinCount++
  })

  // 计算每天的参与人数
  dateStatsMap.forEach((stats, date) => {
    const dateUserIds = new Set(
      filteredData
        .filter(item => item.checkinTime.startsWith(date))
        .map(item => item.userId)
    )
    stats.userCount = dateUserIds.size
  })

  const dateStats = Array.from(dateStatsMap.values()).sort((a, b) => {
    return new Date(b.date).getTime() - new Date(a.date).getTime()
  })

  const statistics: CheckinStatistics = {
    totalCount,
    clockInCount,
    clockOutCount,
    userCount,
    projectCount: projectStats.length,
    dateCount: dateStats.length,
    userStats,
    projectStats,
    dateStats
  }

  return statistics
}
