/**
 * 获取打卡记录列表 Mock 函数
 */
export function getCheckinListMock(params: CheckinListParams) {
  const { projectId, userId, checkinType, startTime, endTime, page = 1, pageSize = 20 } = params
  let filteredData = [...checkinRecords]

  // 按项目筛选
  if (projectId) {
    filteredData = filteredData.filter(item => item.projectId === projectId)
  }

  // 按人员筛选
  if (userId !== undefined && userId !== null && userId !== '') {
    const userIdValue = typeof userId === 'string' ? parseInt(userId) : userId
    filteredData = filteredData.filter(item => item.userId === userIdValue)
  }

  // 按打卡类型筛选
  if (checkinType !== undefined && checkinType !== null && checkinType !== '') {
    const typeValue = typeof checkinType === 'string' ? parseInt(checkinType) : checkinType
    filteredData = filteredData.filter(item => item.checkinType === typeValue)
  }

  // 按时间范围筛选
  if (startTime) {
    filteredData = filteredData.filter(item => item.checkinTime >= startTime)
  }
  if (endTime) {
    filteredData = filteredData.filter(item => item.checkinTime <= endTime)
  }

  // 按打卡时间倒序排序
  filteredData.sort((a, b) => {
    return new Date(b.checkinTime).getTime() - new Date(a.checkinTime).getTime()
  })

  // 分页
  const start = (page - 1) * pageSize
  const end = start + Number(pageSize)
  const list = filteredData.slice(start, end)

  return {
    list,
    total: filteredData.length
  }
}

